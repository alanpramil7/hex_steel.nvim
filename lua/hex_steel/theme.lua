local function extend(base, extra)
  return vim.tbl_extend("force", base or {}, extra or {})
end

-- Helix stores styling as a list of modifiers. Convert those to Neovim's
-- highlight attributes so we can keep a single mapping source of truth.
local function with_style(spec, modifiers)
  local out = vim.deepcopy(spec)
  for _, modifier in ipairs(modifiers or {}) do
    if modifier == "bold" then
      out.bold = true
    elseif modifier == "italic" then
      out.italic = true
    elseif modifier == "underlined" then
      out.underline = true
    elseif modifier == "crossed_out" then
      out.strikethrough = true
    end
  end
  return out
end

local function bg(color, transparent)
  if transparent then
    return "NONE"
  end
  return color
end

