-- Populate the terminal palette so :terminal and embedded terminals stay close
-- to the scheme instead of falling back to unrelated default ANSI colors.
function M.apply_terminal()
  local p = palette
  vim.g.terminal_color_0 = p.t1
  vim.g.terminal_color_1 = p.t7
  vim.g.terminal_color_2 = p.diff_plus
  vim.g.terminal_color_3 = p.highlight_two
  vim.g.terminal_color_4 = p.display
  vim.g.terminal_color_5 = p.highlight
  vim.g.terminal_color_6 = p.selection
  vim.g.terminal_color_7 = p.t10
  vim.g.terminal_color_8 = p.t3
  vim.g.terminal_color_9 = p.error
  vim.g.terminal_color_10 = p.diff_plus
  vim.g.terminal_color_11 = p.warning
  vim.g.terminal_color_12 = p.diff_delta
  vim.g.terminal_color_13 = p.t11
  vim.g.terminal_color_14 = p.t8
  vim.g.terminal_color_15 = p.t9
end

function M.load()
  M.apply_terminal()

  -- Set every group explicitly to avoid depending on the user's current theme
  -- or plugin order.
  for group, spec in pairs(M.groups()) do
    vim.api.nvim_set_hl(0, group, spec)
  end
end

return M
